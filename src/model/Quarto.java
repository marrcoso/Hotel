class Quarto {
    private int id;
    private String descricao;
    private int capacidadeHospedes;
    private float metragem;
    private String identificacao;
    private int andar;
    private boolean flagAnimais;
    private String obs;
    private char String;

    public Quarto(int id, String descricao, int capacidadeHospedes, float metragem, String identificacao, int andar, boolean flagAnimais, String obs, char String) {
        this.id = id;
        this.descricao = descricao;
        this.capacidadeHospedes = capacidadeHospedes;
        this.metragem = metragem;
        this.identificacao = identificacao;
        this.andar = andar;
        this.flagAnimais = flagAnimais;
        this.obs = obs;
        this.String = String;
    }  
}
